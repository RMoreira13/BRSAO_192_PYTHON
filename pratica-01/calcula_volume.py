"""
3- Calculadora de Volume
* Crie um programa que calcula o volume de uma caixa retangular. 
* Use as seguintes dimensões:

* Comprimento: 12 cm
* Largura: 14 cm
* Altura: 20 cm
"""
comprimento = 12 
largura = 14
altura = 20

calcula_volume = comprimento * largura * altura
resultado = calcula_volume
print("O volume calculado é: ", resultado,"cm³")